public class ModArquiConfiguracao {
    private String numFilial;
    private String numCaixa;
    private String numIp;
    private String numImpressora;

    public String getNumFilial() {
        return numFilial;
    }

    public void setNumFilial(String numFilial) {
        this.numFilial = numFilial;
    }

    public String getNumCaixa() {
        return numCaixa;
    }

    public void setNumCaixa(String numCaixa) {
        this.numCaixa = numCaixa;
    }

    public String getNumIp() {
        return numIp;
    }

    public void setNumIp(String numIp) {
        this.numIp = numIp;
    }

    public String getNumImpressora() {
        return numImpressora;
    }

    public void setNumImpressora(String numImpressora) {
        this.numImpressora = numImpressora;
    }
}
