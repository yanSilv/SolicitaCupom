    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ArrayList<String> dados = new ArrayList<String>();
        dados.add(String.valueOf(jCsolicitacao.getSelectedIndex()));
        dados.add((String) jCFilial.getSelectedItem());
        dados.add((String) jCCaixa.getSelectedItem());
        dados.add(tfData.getText());
        dados.add(tfCupomInicial.getText());
        dados.add(tfCupomfim.getText());
        boolean status;
        try {
            jlResposta.setText("Criar o arquivo de solicitação...");
            status = conSoli.montaSolicitacao(dados);
            if (status) {
                status = conSoli.enviaSolicitacao(dados, listHost, this);
            } else {
                jlResposta.setText("Erro ao criar o arquivo de solicitação...");
            }
        } catch (IOException ex) {
            Logger.getLogger(Solicita.class.getName()).log(Level.SEVERE, null, ex);
        }
         
    }//GEN-LAST:event_jButton1ActionPerformed

