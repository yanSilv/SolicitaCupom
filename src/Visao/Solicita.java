    private void preencheComoboxFilial(ArrayList<ModHost> listHost) {
        for (int i = 1; i < listHost.size(); i++){
            if (i == 1) {
                jCFilial.addItem(listHost.get(i).getFilial());
                itemAux = listHost.get(i).getFilial();
            } else if(!itemAux.equals(listHost.get(i).getFilial())){
                jCFilial.addItem(listHost.get(i).getFilial());
                itemAux = listHost.get(i).getFilial();
            }
        }
    }
    
    private void preencheComoboxCaixa(String item, ArrayList<ModHost> listHost) {
        jCCaixa.removeAllItems();
        for(int i = 0; i < listHost.size(); i++){
            if (listHost.get(i).getFilial().equals(item)){
                jCCaixa.addItem(listHost.get(i).getCaixa());
            }
        }
    }
